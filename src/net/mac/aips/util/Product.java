package net.mac.aips.util;

public class Product {
	private String bnsId;
	private String bnsName;
	private String bnsImg;
	private String bnsDesc;
	private String bnsCategory;
	private String bnsSubmittime;
	public String getBnsId() {
		return bnsId;
	}
	public void setBnsId(String bnsId) {
		this.bnsId = bnsId;
	}
	public String getBnsName() {
		return bnsName;
	}
	public void setBnsName(String bnsName) {
		this.bnsName = bnsName;
	}
	public String getBnsImg() {
		return bnsImg;
	}
	public void setBnsImg(String bnsImg) {
		this.bnsImg = bnsImg;
	}
	public String getBnsDesc() {
		return bnsDesc;
	}
	public void setBnsDesc(String bnsDesc) {
		this.bnsDesc = bnsDesc;
	}
	public String getBnsCategory() {
		return bnsCategory;
	}
	public void setBnsCategory(String bnsCategory) {
		this.bnsCategory = bnsCategory;
	}
	public String getBnsSubmittime() {
		return bnsSubmittime;
	}
	public void setBnsSubmittime(String bnsSubmittime) {
		this.bnsSubmittime = bnsSubmittime;
	}
}
