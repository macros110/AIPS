package net.mac.aips.util;

public class News {
	private String nwsId;
	private String nwsTitle;
	private String nwsContent;
	private String nwsAuthor;
	private String nwsSubmittime;
	public String getNwsId() {
		return nwsId;
	}
	public void setNwsId(String nwsId) {
		this.nwsId = nwsId;
	}
	public String getNwsTitle() {
		return nwsTitle;
	}
	public void setNwsTitle(String nwsTitle) {
		this.nwsTitle = nwsTitle;
	}
	public String getNwsContent() {
		return nwsContent;
	}
	public void setNwsContent(String nwsContent) {
		this.nwsContent = nwsContent;
	}
	public String getNwsAuthor() {
		return nwsAuthor;
	}
	public void setNwsAuthor(String nwsAuthor) {
		this.nwsAuthor = nwsAuthor;
	}
	public String getNwsSubmittime() {
		return nwsSubmittime;
	}
	public void setNwsSubmittime(String nwsSubmittime) {
		this.nwsSubmittime = nwsSubmittime;
	}
}
